package basicprogram;

public class Arithmetic_Operators
{

	public static void main(String[] args)
	{
		int x = 6;
	    int y = 3;
	    System.out.println("x+y="+(x + y));
	    System.out.println("x-y="+(x - y));
	    System.out.println("x*y="+(x * y));
	    System.out.println("x/y="+(x / y));
	    System.out.println("x%y="+(x % y));
	    
	}

}
